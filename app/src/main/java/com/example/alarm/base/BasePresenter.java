package com.example.alarm.base;

/**
 * Base的好处 可以什么都不写,但一定要预留出来
 * 这个泛型的设计 : 在上面传入泛型,就可以持有VM对象了
 */

public abstract class BasePresenter<M extends BaseModel,V extends BaseView> {
    //可以写构造,可以写方法

    public M mModel;

    public V mView;

    public BasePresenter(){}

    public void setVM(V mView ,M mModel){
        this.mModel = mModel;
        this.mView = mView;
    }


}
